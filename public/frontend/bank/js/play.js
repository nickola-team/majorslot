function chatPop() {
	var pop = "chat.html";
	var mw = screen.availWidth; //화면 넓이
	var mh = screen.availHeight; //화면 높이
	var w = 309; //팝업창 가로
	var h = 417; //팝업창 세로
	var left = (mw - w) / 2; //팝업창 x값
	var top = (mh - h) / 2; //팝업창 y값
	var popOpen = "width="+w+",height="+h+",top="+top+",left="+left+",resizable=no,scrollbars=no,status-no,toolbar=no,menubar=no,location=no,directories=no;";
	window.open(pop,"",popOpen);
}