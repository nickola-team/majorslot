        public function findZokbos($reels, $firstSymbol, $repeatCount, $strLineWin){
            $wild = '2';
            $bPathEnded = true;
            if($repeatCount < 3){
                for($r = 0; $r < 8; $r++){
                    if($firstSymbol == $reels[$repeatCount][$r] || $reels[$repeatCount][$r] == $wild || $firstSymbol == $wild){
                        $subFirstSymbol = $firstSymbol;
                        if($subFirstSymbol == $wild){
                            $subFirstSymbol = $reels[$repeatCount][$r];
                        }
                        $this->findZokbos($reels, $subFirstSymbol, $repeatCount + 1, $strLineWin . '~' . ($repeatCount + $r * 3));
                        $bPathEnded = false;
                    }
                }
            }
            if($bPathEnded == true){
                if($repeatCount >= 3){
                    $winLine = [];
                    $winLine['FirstSymbol'] = $firstSymbol;
                    $winLine['RepeatCount'] = $repeatCount;
                    $winLine['StrLineWin'] = $strLineWin;
                    array_push($this->winLines, $winLine);
                }
            }
